<?php

namespace App\Models;

Use Core\Model;

Class Authors extends Model
{
    private $id;
    private $name;
    protected $table = 'authors';

    public function __construct()
    {
    }

    public function getId()
    {
        return $this->id;
    }

    public function setId($id)
    {
        $this->id = $id;
        return $this;
    }

    public function getName()
    {
        return $this->name;
    }

    public function setName($name)
    {
        $this->name = $name;
        return $this;
    }
}